//EXPONENTIATION (**)
//Elevacion de un numero, su potencia. Equivalente a Math.pow excepto que tambien acepto BigInts como operandos. El operador (**) es asociativo a la derecha
console.log('=========== Exponentiation ===========');
console.log(3 ** 4); // 81
console.log(10 ** -2); // 0.01
console.log(2 ** 3 ** 2); //512
console.log((2 ** 3) ** 2); //64